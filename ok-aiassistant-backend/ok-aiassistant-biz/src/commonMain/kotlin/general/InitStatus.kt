package ru.otus.otuskotlin.aiassistant.biz.general

import ru.otus.otuskotlin.aiassistant.cor.ICorChainDsl
import ru.otus.otuskotlin.aiassistant.cor.worker
import AppContext
import models.AIState

fun ICorChainDsl<AppContext>.initStatus(title: String) = worker() {
    this.title = title
    this.description = """
        Этот обработчик устанавливает стартовый статус обработки. Запускается только в случае не заданного статуса.
    """.trimIndent()
    on { state == AIState.NONE }
    handle { state = AIState.RUNNING }
}
